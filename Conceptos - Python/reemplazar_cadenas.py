"""
Este codigo se uso para reemplazar palabras de una cadena ya existente usando replace()
"""
nombre = "Marlon Salazar"

cadena = f"Hola Mundo, Soy {nombre} y es un gusto conocerlos"
print(cadena)

remplazar_cadena = cadena.replace("Mundo","Planeta Tierra")
print(remplazar_cadena)

remplazar_cadena = cadena.replace("gusto","placer")
print(remplazar_cadena)